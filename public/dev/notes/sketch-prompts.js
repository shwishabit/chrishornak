// Lookup helper. Mirrors quotes.js: day-of-year (1..366) modulo length.
// Same date → same prompt every render. New day → new prompt. No re-roll.
function sketchPromptForDate(d) {
  const date = d || new Date();
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = (date - start) + ((start.getTimezoneOffset() - date.getTimezoneOffset()) * 60 * 1000);
  const oneDay = 1000 * 60 * 60 * 24;
  const day = Math.floor(diff / oneDay); // 1..366
  const idx = (day - 1) % SKETCH_PROMPTS.length;
  return SKETCH_PROMPTS[Math.max(0, idx)];
}

if (typeof window !== "undefined") {
  window.SKETCH_PROMPTS = SKETCH_PROMPTS;
  window.sketchPromptForDate = sketchPromptForDate;
}
